	public abstract class MagicImage
	{
		// Translated into slopes, so right border extends to right edge
		// Start with 0s so the collection doesn't turn up empty
		public abstract int[,] GetImage();
		public abstract int ImageHeight { get; }
		public abstract int ImageWidth { get; }

		// Helper Functions
		public static int[,] GetSlopes(int[,] AImage)
		{
			int[,] LReturn = new int[AImage.GetLongLength(0), AImage.GetLongLength(1) - 1];
			for (int i = 0; i < LReturn.GetLongLength(0); i++)
				for (int j = 0; j < LReturn.GetLongLength(1); j++)
					LReturn[i,j] = (AImage[i,j+1] - AImage[i,j])*1;
			return LReturn;
		}

	}

