		#region MagicImage Members

		public override int[,] GetImage()
		{

			int[,] LReturn = new int[ImageHeight, ImageWidth];

			for (int i = 0; i < ImageHeight; i++)
				for(int j = 0; j < ImageWidth; j++)
				{

					if (i < FPadding || j < FPadding || i + FPadding >= ImageHeight || j + FPadding >= ImageWidth)
						LReturn[i,j] = 0;
					else
					{
						Color LColor = FImage.GetPixel(j - FPadding, i - FPadding);
						switch (LColor.ToArgb())
						{
							case -65536: //red
								LReturn[i,j] = 4;
								break;
							case -16711936: //green
							case -16777216: // black
								LReturn[i,j] = 1;
								break;
							case -16776961: // blue
								LReturn[i,j] = 0;
								break;
						}
					}


				}
			return LReturn;
		}

		public override int ImageHeight { get { return FImage.Height + 2 * FPadding; } }

		public override int ImageWidth { get { return FImage.Width + 2 * FPadding; } }

		#endregion

	}

	public class PacMan : MagicImage
	{
		public PacMan()
		{
			FOpenFrame = 1;
			FOpenFrameChange = -1;

			FHeight = 30;
			FWidth = 80;
			FX = 16;
			FDX = -1;
			FY = 15;

			FPips = new bool[FCNumberOfPips];
			SetPips(false);
		}

		// Image Vars;
		private int FHeight;
		private int FWidth;

		// Existence Data
		private bool[] FPips;

		// Position Vars
		private int FDX;
		private int FX;
		private int FY;

		// keeping track of frame (pacman alternates)
		private int FOpenFrame;
		private int FOpenFrameChange;
		private int FNextPip;

		// Constants
		private const int FCNumberOfOpenFrames = 10;
		private const int FCNumberOfPips = 3;

		
		private int GetFrameNumber()
		{
			FOpenFrame += FOpenFrameChange;
			if (FOpenFrame == 0)
			{ // close
				FOpenFrameChange *= -1;
				if (FNextPip >= 0)
					FPips[FNextPip--] = false;
			}
			if (FOpenFrame == FCNumberOfOpenFrames - 1)
			{
				FOpenFrameChange *= -1;
			}
			FX += FDX;
			if (FX > FWidth - 15)
			{ // right edge
				FDX *= -1;
				SetPips(true);
//				FOpenFrame;
			}
			if (FX <= 15)
			{ // left edge
				FDX *= -1;
				FOpenFrame = FCNumberOfOpenFrames - 1;
				FOpenFrameChange = -1;
				// big pip (in case it doesn't close all the way
				FPips[0] = false;

			}
			return FOpenFrame;
		}
		
		private void SetPips(bool AValue)
		{
			for (int i = 0; i < FCNumberOfPips; i++)
			{
				FPips[i] = AValue;
			}
			FNextPip = FCNumberOfPips - 1;
		}

		#region MagicImage Members

		public override int[,] GetImage()
		{
			int ey = FY - 5;
			double r = 10.5;
			double er = 1.5;
			double pr = 2.5;
			double t = Math.PI/4 / FCNumberOfOpenFrames * GetFrameNumber() ;
			int[,] LReturn = new int[FHeight, FWidth];
			for (int i = 0; i < LReturn.GetLongLength(0); i++)
				for (int j = 0; j < LReturn.GetLongLength(1); j++)
				{
					// Set Default
					LReturn[i,j] = 0;
					// Large Pip
					// position = 15,15
					// Pips
					// Max pip position = LWidth - 15 - r
					for (int p = 0; p < FCNumberOfPips; p++)
					{
						double LR = (p == 0 ? pr : er);
						double LX = 15 + 17 * p;
						if (FPips[p])
							LReturn[i,j] = ((  ((LX-j)*(LX-j) + (15-i)*(15-i)) <= LR*LR  ) ? 1 : LReturn[i,j]);
					}
					// PacMan Body
					LReturn[i,j] = ((  ((0.75 * (FX-j)*(FX-j) + (FY-i)*(FY-i)) <= r*r) && !((Math.Atan((double)Math.Abs((FY-i))/Math.Abs((FX-j))) < t) && (j * FDX > FX * FDX))  ) ? 2 : LReturn[i,j] );
					// PacMan Eye
					LReturn[i,j] = ((  ((FX-j)*(FX-j) + (ey-i)*(ey-i)) <= er*er  ) ? 1 : LReturn[i,j] );

				}
			return LReturn;
		}

		public override int ImageHeight { get { return FHeight; } }
		public override int ImageWidth { get { return FWidth; } }

		#endregion

	}

	public class PacMan2 : MagicImage
	{
		#region MagicImage Members
		public override int[,] GetImage()
		{
			int[,] LReturn = new int[ImageHeight, ImageWidth];
			for (int i = 0; i < ImageHeight; i++)
				for(int j = 0; j < ImageWidth; j++)
				{
					int ARadius = 100;
					int ARS = ARadius * ARadius;
					int AXCenter = ImageWidth / 2;
					int AYCenter = ImageHeight / 2;
					int ADXS = i - AXCenter;
					ADXS *= ADXS;
					int ADYS = j - AYCenter;
					ADYS *= ADYS;
					int ADistanceS = ADXS + ADYS;

					

					if (ADistanceS > ARS)
						LReturn[i,j] = 0;
					else
						LReturn[i,j] = (int)Math.Floor(Math.Sqrt(ARS - ADXS - ADYS) / Math.PI);
						
					

					/*
					LReturn[i,j] = ( j < 400 || j > 500 || i < 100 || i > 500 ) ? 0 : 1 ;
					*/


				}
			return LReturn;

		}

